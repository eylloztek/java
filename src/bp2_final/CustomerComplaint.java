    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        if (txt_explain.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please explain your complaint.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                Connection con = getConnection();               

                String id = randomValue(); //assigning random value to ID.
                boolean isExists = false;
                while (isExists) {
                    id = randomValue();
                    Statement s3 = con.createStatement();
                    String query4 = "SELECT * FROM TBL_COMPLAINTS";
                    ResultSet rs3 = s3.executeQuery(query4);

                    while (rs3.next()) {
                        if (rs3.getString("ID").equals(id)) { //if random value is created before, it will create a new random value.
                            isExists = true;
                            break;
                        }
                    }
                }

                con.close();
                String query = "INSERT INTO TBL_COMPLAINTS (ID,COMPLAINTTYPE,COMPLAINT,STATUS) VALUES ('" + id + "','" + cbox_comp.getSelectedItem().toString() + "','" + txt_explain.getText() + "','" + false + "')";
                Connection con2 = getConnection();
                Statement pst = con2.createStatement();
                pst.executeUpdate(query);
                
                txt_compNo.setText(id + "");

                con2.close();
                JOptionPane.showMessageDialog(this, "Your complaint is sent.", "Info", JOptionPane.INFORMATION_MESSAGE);

            } catch (SQLException ex) {
                System.out.println("Unable to connect database");
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_submitActionPerformed

